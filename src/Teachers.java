    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            con=javaconnection.ConnectDB();

            String sql= "insert into teacher(tea_name, username, tea_gender, tea_phone,tea_email,tea_program, tea_info,tea_title)  values (?,?,?,?,?,?,?,?)";

            pst=con.prepareStatement(sql);
            pst.setString(1,tea_name.getText());
            pst.setString(2,tea_username.getText());
            pst.setString(3, (String) jComboBox1.getSelectedItem());
            pst.setString(4,tea_phone.getText());
            pst.setString(5,tea_email.getText());
            pst.setString(6,tea_program.getText());
            pst.setString(7,tea_info.getText());
            pst.setString(8,tea_title.getText());

            pst.execute();
            JOptionPane.showMessageDialog(this,"Successfully saved","Record",JOptionPane.INFORMATION_MESSAGE);
            Get_Data();

        }catch(Exception ex){

        }

    }//GEN-LAST:event_jButton1ActionPerformed

